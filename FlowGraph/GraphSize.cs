namespace FlowGraph
{
    public class GraphSize
    {
        public GraphSize(int width, int height)
        {
            Width = width;
            Height = height;
        }

        /// <summary>
        /// Width
        /// </summary>
        public int Width { get; }

        /// <summary>
        /// Height
        /// </summary>
        public int Height { get; }
    }
}
