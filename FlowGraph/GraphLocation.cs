namespace FlowGraph
{
    public class GraphLocation
    {
        public GraphLocation(int x, int y)
        {
            X = x;
            Y = y;
        }

        /// <summary>
        /// X
        /// </summary>
        public int X { get; }

        /// <summary>
        /// Y
        /// </summary>
        public int Y { get; }
    }
}
